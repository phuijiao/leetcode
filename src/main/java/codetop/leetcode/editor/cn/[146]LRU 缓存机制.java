class LRUCache {

    public static class Node {
      int key;
      int val;
      Node prev;
      Node next;

      public Node(){};
      public Node(int key, int val){
        this.key = key;
        this.val = val;
      };
    }

    Node head, tail;
    int capacity, size;
    Map<Integer, Node> map;
    public LRUCache(int capacity) {
      this.capacity = capacity;
      this.size = 0;
      head = new Node();
      tail = new Node();
      head.next = tail;
      tail.prev = head;
      map = new HashMap<>();
    }
    
    public int get(int key) {
      Node node = map.get(key);
      if (node == null) {
        return -1;
      }
      moveToHead(node);
      return node.val;
    }

    public void put(int key, int value) {
      Node node = map.get(key);
      if (node != null) {
        node.val = value;
        moveToHead(node);
        return;
      }

      if (size == capacity) {
        remove(tail.prev);
      }
      addHead(new Node(key, value));
    }

    private Node remove(Node node) {
      node.prev.next = node.next;
      node.next.prev = node.prev;
      size--;
      map.remove(node.key);
      return node;
    }

    private void addHead(Node node) {
      node.next = head.next;
      node.next.prev = node;
      head.next = node;
      node.prev = head;
      size++;
      map.put(node.key, node);
    }
    private void moveToHead(Node node) {
      remove(node);
      addHead(node);
    }

}

