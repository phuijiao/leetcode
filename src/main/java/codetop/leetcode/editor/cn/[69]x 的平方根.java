class Solution {

    public int mySqrt(int x) {
        long res = -1;
        long l = 0, r = x, m;
        while (l <= r) {
            m = (l + r) >> 1;
            if (m * m <= x) {
                res = m;
                l = m + 1;
            } else {
                r = m - 1;
            }
        }
        return (int) res;
    }
}
