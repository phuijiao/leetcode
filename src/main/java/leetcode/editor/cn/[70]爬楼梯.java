class Solution {
    public int climbStairs1(int n) {
      if (n <= 3) {
        return n;
      }
      int l = 1, r = 2, k = 3;
      for (int i = 2; i < n; i++) {
        k = l + r;
        l = r;
        r = k;
      }
      return k;
    }

    public int climbStairs(int n) {
      if (n <= 3) {
        return n;
      }
      return climbStairs(n - 1) + climbStairs(n - 2);
    }
}
