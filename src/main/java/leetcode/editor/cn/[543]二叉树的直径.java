class Solution {
    private int ans;
    public int diameterOfBinaryTree(TreeNode root) {
      ans = 0;
      depth(root);
      return ans;
    }

    private int depth(TreeNode root) {
      if (root == null) {
        return 0;
      }
      int l = depth(root.left);
      int r = depth(root.right);
      ans = Math.max(ans, l + r);
      return Math.max(l, r) + 1;
    }
  }
