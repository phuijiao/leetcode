class Solution {
    public List<List<Integer>> subsets(int[] nums) {
      ArrayList<List<Integer>> res = new ArrayList<>();
      res.add(new ArrayList<>());
      int len = nums.length;
      for (int i = 0; i < len; i++) {
        int size = res.size();
        for (int j = 0; j < size; j++) {
          ArrayList<Integer> list = new ArrayList<>(res.get(j));
          list.add(nums[i]);
          res.add(list);
        }
      }
      return res;
    }
  }
