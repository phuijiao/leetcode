class Solution {
    public List<Integer> rightSideView(TreeNode root) {
      ArrayList<Integer> res = new ArrayList<>();
      if (root == null) {
        return res;
      }
      LinkedList<TreeNode> queue = new LinkedList<>();
      queue.add(root);
      while (!queue.isEmpty()) {
        int size = queue.size();
        for (int i = 0; i < size; i++) {
          TreeNode node = queue.remove();
          if (node.right != null) {
            queue.add(node.right);
          }
          if (node.left != null) {
            queue.add(node.left);
          }
          if (i == 0) {
            res.add(node.val);
          }
        }
      }
      return res;
    }
}
