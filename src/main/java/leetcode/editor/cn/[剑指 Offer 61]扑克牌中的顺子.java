class Solution {
    public boolean isStraight(int[] nums) {
      HashSet<Integer> set = new HashSet<>();
      int max = Integer.MIN_VALUE, min = Integer.MAX_VALUE;
      int count = 0;
      for (int num : nums) {
        if (num == 0) {
          continue;
        }
        max = Math.max(max, num);
        min = Math.min(min, num);
        if (!set.add(num)) {
          return false;
        }
      }

      return (max - min) <= 4;
    }
}
