class Solution {
    public ListNode swapPairs1(ListNode head) {
      ListNode root = new ListNode(0, head);
      ListNode tmp = root;
      ListNode t;
      while (tmp.next != null && tmp.next.next != null) {
        t = tmp.next.next;
        tmp.next.next = t.next;
        t.next = tmp.next;
        tmp.next = t;
        tmp = tmp.next.next;
      }
      return root.next;
    }

    // 1  3 4 -> 1 3 2 4
    public ListNode swapPairs(ListNode head) {
      ListNode root = new ListNode(0, head);
      ListNode t1 = root;
      ListNode t2 = t1.next;
      while (t2 != null && t2.next != null) {
        t1.next = t2.next;
        t1 = t1.next;
        t2.next = t1.next;
        t1.next = t2;
        t1 = t2;
        t2 = t2.next;
      }
      return root.next;
    }
}
